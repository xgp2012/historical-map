// 简化的世界地图 - 主要大陆轮廓
// 坐标格式: [经度, 纬度]

export interface Point {
  x: number;
  y: number;
}

export interface Continent {
  name: string;
  paths: Point[][];
}

// 等距圆柱投影: 经度 -> x, 纬度 -> y
export function project(lon: number, lat: number, width: number, height: number): Point {
  const x = ((lon + 180) / 360) * width;
  const y = ((90 - lat) / 180) * height;
  return { x, y };
}

